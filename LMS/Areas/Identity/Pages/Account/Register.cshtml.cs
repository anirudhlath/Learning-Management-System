        /// <summary>
        /// Create a new user account and insert into your database (the one you designed in phase1/2 not the
        /// Authorization DB that dotnet creates for you.  Other code does that for you)
        /// </summary>
        /// <returns>
        /// A new unique uID that is not currently in use by any student, professor, or administrator
        /// </returns>
        /// <param name="firstName"></param>
        /// <param name="lastName"></param>
        /// <param name="DOB"></param>
        /// <param name="departmentAbbrev"></param>
        /// <param name="role"></param>
        string CreateNewUser(string firstName, string lastName, DateTime DOB, string departmentAbbrev, string role)
        {
            uint num = 1;
            
            // Check if database has entries
            if (db.Users.Any())
            {
                var last = db.Users.Last(); // Check last entry
                var uid = uint.Parse(last.UId.Remove(0,1)); // Remove the 'u'
                if (uid > 9999999) // Check if there is no space for more users
                {
                    Console.WriteLine("Database full.");
                    return null;
                }

                num += uid;
            }
            
            // Prepare string format
            const string fmt = "u0000000";
            var userId = num.ToString(fmt);
            
            var user = new User
            {
                UId = userId,
                FirstName = firstName,
                LastName = lastName,
                Dob = DOB
            };
            

            if (role.ToLower() == "student")
            {
                var student = new Student
                {
                    UId = userId,
                    Major = departmentAbbrev
                };
                user.Student = student;
                db.Users.Add(user);
                db.Students.Add(student);
            }
            else if (role.ToLower() == "professor")
            {
                var professor = new Professor
                {
                    UId = userId,
                    SubjectAbb = departmentAbbrev
                };
                user.Professor = professor;
                db.Users.Add(user);
                db.Professors.Add(professor);
            }
            else if (role.ToLower() == "administrator")
            {
                var administrator = new Administrator
                {
                    UId = userId
                };
                user.Administrator = administrator;
                db.Users.Add(user);
                db.Administrators.Add(administrator);
            }
            else
            {
                Console.WriteLine("Invalid Role.");
                return null;
            }

            db.SaveChanges();
            return userId;
        }

